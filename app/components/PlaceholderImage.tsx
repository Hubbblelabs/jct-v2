import React from 'react';

interface PlaceholderImageProps {
    width?: number | string;
    height?: number | string;
    text?: string;
    className?: string;
    variant?: 'campus' | 'students' | 'lab' | 'library' | 'building' | 'hero' | 'default';
}

const gradients: Record<string, string> = {
    campus: 'from-primary via-primary-light to-secondary',
    students: 'from-secondary via-secondary-dark to-primary',
    lab: 'from-tertiary via-tertiary-dark to-primary',
    library: 'from-accent/80 via-amber-600 to-primary',
    building: 'from-slate-700 via-slate-800 to-primary',
    hero: 'from-primary via-primary-light to-secondary',
    default: 'from-neutral-700 via-neutral-800 to-neutral-900',
};

const icons: Record<string, React.ReactNode> = {
    campus: <CampusIcon />,
    students: <StudentsIcon />,
    lab: <LabIcon />,
    library: <LibraryIcon />,
    building: <BuildingIcon />,
    hero: <HeroIcon />,
    default: <ImageIcon />,
};

export default function PlaceholderImage({
    width = '100%',
    height = '100%',
    text,
    className = '',
    variant = 'default',
}: PlaceholderImageProps) {
    const gradient = gradients[variant] || gradients.default;
    const icon = icons[variant] || icons.default;

    return (
        <div
            className={`relative overflow-hidden bg-gradient-to-br ${gradient} ${className}`}
            style={{ width, height }}
        >
            {/* Pattern Overlay */}
            <div className="absolute inset-0 opacity-10">
                <svg className="w-full h-full" xmlns="http://www.w3.org/2000/svg">
                    <defs>
                        <pattern id="grid" width="40" height="40" patternUnits="userSpaceOnUse">
                            <path d="M 40 0 L 0 0 0 40" fill="none" stroke="white" strokeWidth="0.5" />
                        </pattern>
                    </defs>
                    <rect width="100%" height="100%" fill="url(#grid)" />
                </svg>
            </div>

            {/* Center Content */}
            <div className="absolute inset-0 flex flex-col items-center justify-center text-white/30">
                <div className="w-16 h-16 mb-3">
                    {icon}
                </div>
                {text && (
                    <span className="text-sm font-medium uppercase tracking-wider">{text}</span>
                )}
            </div>

            {/* Gradient Overlay */}
            <div className="absolute inset-0 bg-gradient-to-t from-black/30 to-transparent" />
        </div>
    );
}

